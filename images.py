import framebuf

class Bitmap:

    def __init__(self,w,h,d):
        self.width = w
        self.height = h
        self.data = d

    def blit(self, target, x, y, fg, bg):
        buf = bytearray(self.data)
        fbc = framebuf.FrameBuffer(buf, self.width, self.height, framebuf.MONO_HMSB)

        for r in range(self.height):
            for c in range(self.width):
                if fbc.pixel(c, r):
                    target.pixel(x + c, y + r, fg)
                else:
                    target.pixel(x + c, y + r, bg)


